          <h3>Zip Drive</h3> 
          <p>A removable floppy disk storage system that was introduced by Iomega in late 1994. Considered medium-to-high-capacity at the time of its release, Zip disks were originally launched with capacities of 100 MB, then 250 MB, and finally 750 MB.</p>
          <p>The format became the most popular of the superfloppy products which filled a niche in the late 1990s portable storage market. However, it was never popular enough to replace the 3+1⁄2-inch floppy disk. Zip drives fell out of favor for mass portable storage during the early 2000s as CD-RW and USB flash drives became prevalent. The Zip brand later covered internal and external CD writers known as Zip-650 or Zip-CD, despite the dissimilar technology.</p>
