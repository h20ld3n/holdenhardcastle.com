        <h3>Composition</h3>
        <p><strong>The syntactical guidelines for visual literacy</strong></p>
        <p>The process of composition is the most crucial step in visual problem solving. The rsult of the compositional decisions set the purpose and meaning of the visual statemen and carry strong implications for what the viewer recieves. It is at the vital stage in the creative process that the visual communicator has the strongest control of the woek and the greatest opportunity to express the total mood the work is intended to convey. But the visual mode offers no proscribed structural systems that are absolut. How can we gain control of out complex visual means with some certainty of the shared meaning in the final results? IN language syntax means the orderly arrangement of words in their appropriate form and order. The rules are defined: all one has to do is learn them and use them intelligently. But syntax in the context of visual literacy can only mean the orderly arrangement of parts, leaving us with the problem of how we can approach the process of composition with intelligence and knowledge og how compistional decisions will affect the final result. There are no absolute rules, but there is a great deal of understanding of what will occur in terms of meaning if we make certain arrangements of the parts toward organizing and orchestrating the visual means. Many of the guidelines for understanding the meaning in visual form, the syntactical potential of structure in the visual literacy, stem from the investigation of the process of human perception.</p>
