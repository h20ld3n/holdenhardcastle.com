        <h3>Perception and visual communication</h3>
        <p><strong>Meaning in visual message-making lies not only in the cumulative effects of the arrangement of the basic elements but also in the perceptual mechanism that is universally shared by the human organism.</strong></p>
        <p>More simply put: we create a design out of many colors and shapes and textures and tones and relative proportions; we relate these elements interactively; we intend meaning. The result is the composition, the artist's or photographer's or designer's intention. It is their input. Seeing is another and seperate step in visual communication. It is the process of absorbing information into the nervous system through the eyes, the sense of sight. This process and capacity is shared by all people on a more or less common basis, finding its signifigance in terms of shared meaning. The two seperate steps, seeing and designing and / or making are interdependant for bothing meaning in a general sense and message in the case of attempting to respond to a specific communication. Between the general meaning, mood, or ambience of visual inforamtion and a specific, defined message lies yet another area of visual meaning, functionality, in the objects that are designed, made, and manufactured to serve a purpose. While it would seem that the message of such works is secondary to their viability, the facts prove otherwise. Clothes, houses, public buildingsm even whittling and scrimshaw of amatuer craftsman tell us an enourmous amount about the people who designed and chose them. And our understandin of a culture depends on out study of the world they built and the tools and artificats an art they created.</p>
