          				<p>In 2006, I wrote and directed a short film, "Leader of the Pack," based on the 1950s song by the Shangri-Las. I also began adapting "Boyz 'R' Us," a novel for young readership published by Random House Australia and written by Scott Monk, into a feature-length screenplay. Issues of loyalty to the original work, moral responsibility to the audience in delivering the idea presented themselves during the creation of "Leader of the Pack" and while "Boyz 'R' Us."</p>
                        <p><strong>Boyz 'R' Us And Its Context</strong></p>
                        <p>"Boyz 'R' Us" does not exist in a vacuum and certainly would not be the first film of its kind. It is, however, my first attempt at adapting a major written work into something suitable for the screen.  At its core, "Boyz 'R' Us" is a coming of age story.</p>
                        <p>com&#xb7;ing&#xb7;of&#xb7;age&#xb7;stor&#xb7;y (n) - A series of events affects a small and particular group of people, where a life-changing event occurs and is unable or unwilling to return to the previous state; the change is not limited to the characters in the movie.</p>
                        <p>While there are many coming of age movies, "Boyz 'R' Us" potentially exists with films like my own short "Leader of the Pack," "City of God," "Kids," "Chopper," and "Somersault." While these films are very different from each other, there are similarities between them and "Boyz 'R' Us." The films relate to and should be an inspiration for "Boyz 'R' Us" in delivery and message. The films also show the potential for success of "Boyz 'R' Us."</p>
                        <p>"Leader of the Pack" is a short film based on the Shangri-Las 1950s pop song of the same name. The film looks at and plays with the relationships between friends and family. The film has been updated for the 21st century but remains true to the original story. The kids care about very little except maybe one another. If only briefly, the film touches on the insecurities of youth, the materialism of our culture, and yes, the fact that kids are kids and do stupid juvenile things.</p>
                        <p>When I first set out writing "Leader of the Pack," it was not "Leader of the Pack." I didn't know what it was. But it had to be about kids, and I wanted the kids to be authentic. I had all sorts of situations and bits and pieces of dialogue ready to go, but I needed a story. The 1950's song "Leader of the Pack" came blaring out of the crappy portable speakers I own. Here was a song that had it all. My inspiration arrived via iPod. The first draft quickly began to take shape. The period seemed to be a big issue. When and where to place the story?</p>
