          				<h3>Modular relationships</h3>
						<p>A <i>module</i> is a basic elementary unit that can be combined and repeated to become the basis for the whole. The physical world with its molecular and crystal structure, all life with its cellular structure, and much art and design are based on modular relationships. The most frequent use of modular structure in a graphic design is the use of a modular grid of horizontal and vertical linear divisions. A grid structure can take many forms: a regular sequence of squares or rectangels; a format of columns for publication design; and complex modular relationships that allow for diverse typographic treatments and a lage variety of image sizes and shapes.</p>
						<p>A <i>visual program</i> is a system of parameters used consistently to unify a series or sequence of designs. Grid structure, consistent use of type sizes and styles, placment of page numbers, and a color plan are factors that might beincluded. Russion constructivist designers of the 1920s made important contributions to the development of visual programs. The trilingual 1925 book, <i>The Ism of Art</i>, designed by El Lissitzky is a milestone in the use of a modular grid structure and mathematical measurement to bring order to complex visual material. Lissitzky carefully planned a visual program that gives order and consistent graphic properties. The book is organized into two sections: an eleven-page typographic section, followed by a fort-eight page pictorial portfolio presenting major European art movements from 1914 until 1924.</p>
						<p>Lissitzky executed his layouts on graph paper, which imposed the order of modular structure into his work. He specified san serif type, for he and other members of the mondern movement believe that it best expressed the rationalism of the scientific and technological era. Pictorial portfolio pages are anchored by heavy black rules in their top outside corners, adjacent to the name of the movement. Page numbers align with the outside vertical grid line. Visual continuity and immediate identification results, for readers quickly learn to look at the outside top corner for this information. By stripping away their backgrounds to silhoutte many of the photographs, Lissitzky gives them a strong "object quality." Picture numbers are large, which makes them concrete from in the design orginization. These numbers are composed in counterpoint to the illustrations. Lissitzky makes generous use of white space as an important compositional element.</p>
